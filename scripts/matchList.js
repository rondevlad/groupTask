function toggleMoreText() {
    var moreText = document.querySelector('.more-text');
    moreText.style.display = (moreText.style.display === 'none' || moreText.style.display === '') ? 'block' : 'none';
}

function toggleMoreText2() {
    var moreText2 = document.querySelector('.more-text2');
    moreText2.style.display = (moreText2.style.display === 'none' || moreText2.style.display === '') ? 'block' : 'none';
}